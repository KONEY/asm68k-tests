;********** Demo Routines **********
PokePtrs:				; Generic, poke ptrs into copper list
	.bpll:	
	move.l	a0,d2
	swap	d2
	move.w	d2,(a1)		;high word of address
	move.w	a0,4(a1)		;low word of address
	addq.w	#8,a1		;skip two copper instructions
	add.l	d0,a0		;next ptr
	dbf	d1,.bpll
	rts

ClearScreen:			; a1=screen destination address to clear
	bsr	WaitBlitter
	clr.w	$66(a6)		; destination modulo
	move.l	#$01000000,$40(a6)	; set operation type in BLTCON0/1
	move.l	a1,$54(a6)	; destination address
	move.l	#blitsize*bpls,$58(a6)	;blitter operation size
	rts

VBint:				; Blank template VERTB interrupt
	movem.l	d0/a6,-(sp)	; Save used registers
	lea	$dff000,a6
	btst	#5,$1f(a6)	; check if it's our vertb int.
	beq.s	.notvb
	;*--- do stuff here ---*
	moveq	#$20,d0		; poll irq bit
	move.w	d0,$9c(a6)
	move.w	d0,$9c(a6)
	.notvb:	
	movem.l	(sp)+,d0/a6	; restore
	rte

GenerateTexture:
	MOVE.L	SCROLL_DEST,A0
	lea	tt,a1
	move.l	a1,a2
	moveq	#-1,d7
	.a:	
	ror.l	d0,d0
	addq.l	#1+2,d0
	move.b	d0,(a2)+
	dbra	d7,.a
	moveq	#1+2,d4
	.b:	
	moveq	#1+2+3,d6
	.c:	
	move.l	a1,a2
	move.l	a1,a3
	lea	-(1*2*2*2*4*8)(a3),a3
	.d:	
	moveq	#3-2-1,d0
	moveq	#3-2-1,d1
	move.b	-1(a2),d0
	add.b	(1*2*4*8*4*2*1)(a3),d0
	move.b	1(a2),d1
	add.b	(a3)+,d1
	add.w	d0,d1
	lsr.w	#2,d1
	move.b	d1,(a2)+
	dbra	d7,.d
	dbra	d6,.c
	move.l	a1,a2
	.e:	
	move.b	(a2),d0
	lsl.b	#(3-2+1),d0
	bge.s	.f
	not.b	d0
	.f:	
	move.b	d0,(a2)+
	dbra	d7,.e
	dbra	d4,.b
	.g:	
	moveq	#3-2-1,d0
	moveq	#3-2-1,d1
	move.b	(a1)+,d0
	move.b	1(a1),d1
	sub.w	d0,d1
	add.w	#1+10+100,d1
	bpl.s	.h
	moveq	#3-2-1,d1
	.h:	
	moveq	#1+2,d6
	.i:	
	move.b	d1,(a0)+
	dbra	d6,.i
	dbra	d7,.g
	rts

__DITHER_PLANE:
	MOVE.L	SCROLL_DEST,A4
	MOVE.W	#h-1,D4		; QUANTE LINEE
	MOVE.L	#$AAAAAAAA,D5
	.outerloop:		; NUOVA RIGA
	MOVE.W	#(bpl/4)-1,D6	; RESET D6
	NOT.L	D5
	.innerloop:		; LOOP KE CICLA LA BITMAP
	MOVE.L	D5,(A4)+
	DBRA	D6,.innerloop
	DBRA	D4,.outerloop
	RTS

__RANDOMIZE_PLANE:
	MOVE.L	SCROLL_DEST,A4
	MOVE.W	#bpl/2*h-1,D4	; QUANTE LINEE
	.innerloop:		; LOOP KE CICLA LA BITMAP
	BSR.S	_RandomWord
	ROL.W	D4,D5
	MOVE.W	D5,(A4)+
	DBRA	D4,.innerloop
	RTS

	_RandomWord:	
	bsr	_RandomByte
	rol.w	#8,d5
	_RandomByte:
	move.b	$dff007,d5	;$dff00a $dff00b for mouse pos
	move.b	$bfd800,d3
	eor.b	d3,d5
	rts

__CREATESCROLLSPACE:
	MOVEM.L	D0-A6,-(SP)	; SAVE TO STACK
	MOVEQ	#bpls-1,D1	; UGUALI PER TUTTI I BITPLANE
	MOVE.L	#%00000000111111110000000000000000,D2	
	MOVE.L	KONEYBG,A4
	.OUTERLOOP:
	MOVEQ	#0,D6		; RESET D6
	MOVE.W	#bpl*hband-1,D6
	ADD.W	#bpl*(hblit),A4	; POSITIONING
	.INNERLOOP:
	MOVE.B	D2,(A4)+	
	DBRA	D6,.INNERLOOP
	ROR.L	#8,D2		; LAST BITPLANE FILLED
	DBRA	D1,.OUTERLOOP
	MOVEM.L	(SP)+,D0-A6	; FETCH FROM STACK
	RTS

