__DUPLICATE_SCREEN:
	MOVEM.L	D0-A6,-(SP)	; SAVE TO STACK
	LEA	SCREEN1,A0
	LEA	SCREEN2,A1
	;ADD.L	#bpl,A1		; Oppure aggiungi la lunghezza di una linea
	MOVE.L	#h*bpls-1,D1	; LINES
	.OUTERLOOP:
	MOVE.L	#w/16-1,D0	; SIZE OF SOURCE IN WORDS
	.INNERLOOP:
	MOVE.W	(A0)+,(A1)+
	DBRA	D0,.INNERLOOP
	DBRA.W	D1,.OUTERLOOP

	MOVEM.L	(SP)+,D0-A6	; FETCH FROM STACK
	RTS